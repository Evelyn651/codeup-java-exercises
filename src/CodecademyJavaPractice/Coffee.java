package CodecademyJavaPractice;

public class Coffee {
    public static void main(String[] args){
        //initialize cupsOfCoffee loop
        int cupsOfCoffee = 1;
        //while loop with counter
        while(cupsOfCoffee <= 100){
            System.out.println("Fry drinks cup of coffee #" + cupsOfCoffee);
            cupsOfCoffee++;
        }
    }
}
